import java.util.ArrayList;

public class Footwear {
    private String materialMade;
    private String color;
    private String footWare;


    public Footwear(){
    }

    public String getMaterialMade() {
        return materialMade;
    }

    public void setMaterialMade(String materialMade) {
        this.materialMade = materialMade;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getFootWare() {
        return footWare;
    }

    public void setFootWare(String footWare) {
        this.footWare = footWare;
    }


}
